#ifndef Bus_hpp
#define Bus_hpp

#include "MemoryUnits.h"

/**
 * ==================================================================
 * Adress range : Device
 * ==================================================================
 * ------------------------------------------------------------------
 * $0000 - $07FF: 2KB internal RAM
 * ------------------------------------------------------------------
 * $0800 - $0FFF:
 * $1000 - $17FF: Mirrors of $0000 - $07FF
 * $1800 - $1FFF:
 * ------------------------------------------------------------------
 * $2000 - $2007: NES PPU registers
 * ------------------------------------------------------------------
 * $4000 - $4017: NES APU and I/O registers
 * ------------------------------------------------------------------
 * $4018 - $401F: APU and I/O functionality that is normally disabled
 * ------------------------------------------------------------------
 * $4020 - $FFFF: Cartrige space
 * ------------------------------------------------------------------
 * Not mapped   : Open bus
 * ------------------------------------------------------------------
 * ==================================================================
 * 
*/

class CPU;

class Bus{
public:
    word readABUS();
    word writeABUS();
    byte readDBUS();
    byte writeDBUS();
    bit readRW();
protected:
private:
    friend class CPU;

    word ABUS;
    byte DBUS;
    bit RW;
};

#endif